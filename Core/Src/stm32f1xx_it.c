/**
  * @brief This function handles EXTI line[9:5] interrupts.
  */
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */
  HAL_Delay(20);
  if (HAL_GPIO_ReadPin(BUTTON_ENSURE_GPIO_Port, BUTTON_ENSURE_Pin) == GPIO_PIN_RESET)
  {
    // 处理逻辑
    {
      if (status_inside)
      {
        switch (cur_status)
        {
        case MESU_MAIN_PAGE:
          //  通过串口，采用dma 模式发送当前时间 温湿度信息，
          if (A_isopen == FALSE)
          {
            A_isopen = TRUE;
          }
          else
          {
            A_isopen = FALSE;
          }
          break;
        case MESU_CURRENT_TIME:
          //  校准当前时间，通过收串口信息，上次缓存的时间信息
          hour = C_hour;
          minute = C_minute;
          second = C_second;
          break;
        case MESU_TIMER:
          //  开启/暂停计时器的计时
          if (T_isopen)
          {
            T_isopen = FALSE;
          }
          else
          {
            T_isopen = TRUE;
          }
          break;
        default:
          break;
        }
      }
      else
      {
        // 确定键
        status_inside = TRUE;
      }
    }
    // 处理逻辑
    while (HAL_GPIO_ReadPin(BUTTON_ENSURE_GPIO_Port, BUTTON_ENSURE_Pin) == GPIO_PIN_RESET)
      ;
  }
  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(BUTTON_ENSURE_Pin);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */

  /* USER CODE END EXTI9_5_IRQn 1 */
}

